public class Loopy{
	public static void main(String[] args){
		int x = 1;
		System.out.println("Before the cycle starts!");
	while(x < 4){
			System.out.println("Inside the loopy");
			System.out.println("The value of x is " + x);
			x++;
		    }
			System.out.println("After the end of the cycle");
	}
}